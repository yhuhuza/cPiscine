int	ft_atoi(char *str)
{
	int	sum;
	int	q;
	int	min;

	q = 0;
	sum = 0;
	min = 1;
	while ((str[q] >= 9 && str[q] <= 13) || str[q] == 32)
		q++;
	while (str[q] == '+' || str[q] =='-')
	{
		if (str[q] == '-')
			min *= -1;
		q++;
	}
	while (str[q] >= '0' && str[q] <= '9')
	{
		sum = (sum * 10) + str[q] - 48;
		q++;
	}
	return (sum * min);
}
