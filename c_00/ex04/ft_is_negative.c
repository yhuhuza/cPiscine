#include <unistd.h>

void	ft_is_negative(int n)
{
	char	NEG;
	char	POS;

	NEG = 'N';
	POS = 'P';
	if (n >= 0)
	{
		write(1, &POS, 1);
	}
	else
	{
		write(1, &NEG, 1);
	}
}
