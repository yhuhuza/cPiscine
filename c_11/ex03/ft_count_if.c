int	ft_count_if(char **tab, int length, int(*f)(char *))
{
	int	q;
	int	c;

	q = 0;
	c = 0;
	while (q < length)
	{
		if ((*f)(tab[q]) != 0)
			c++;
		q++;
	}
	return (c);
}
