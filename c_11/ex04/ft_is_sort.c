int	ft_is_sort(int *tab, int length, int(*f)(int, int))
{
	int	q;
	int	a;
	int	b;

	q = 0;
	b = 0;
	a = 0;
	while (q < (length - 1))
	{
		if ((*f)(tab[q], tab[q + 1]) >= 0)
			a++;
		if ((*f)(tab[q], tab[q + 1]) <= 0)
			b++;
		q++;
	}
	if (a == q || b == q)
		return (1);
	return (0);
}
