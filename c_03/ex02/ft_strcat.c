char	*ft_strcat(char *dest, char *src)
{
	unsigned int	i;
	unsigned int	r;

	i = 0;
	r = 0;
	while (dest[r] != '\0')
		r++;
	while (src[i] != '\0')
	{
		dest[r] = src[i];
		r++;
		i++;
	}
	dest[r] = '\0';
	return (dest);
}
