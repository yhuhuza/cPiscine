int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	unsigned int	m;

	i = 0;
	m = 0;
	while (s1[i] != '\0' && s2[i] !='\0' && i <= n)
	{
		if (s1[i] == s2[i])
			m = 0;
		if (s1[i] > s2[i] || s1[i] < s2[i])
		{	
			m = s1[i] - s2[i];
			break ;
		}
		i++;
	}
	return (m);
}
